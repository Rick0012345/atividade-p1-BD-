import os
from pymongo import MongoClient
from pymongo.errors import ConnectionFailure, DuplicateKeyError
from datetime import datetime
import json
from config import MongoConfig


class MongoDBCRUD:
    """Classe para realizar opera√ß√µes CRUD no MongoDB"""
    
    def __init__(self, connection_string=None, database_name=None, environment=None):
        """
        Inicializa a conex√£o com o MongoDB
        
        Args:
            connection_string (str, optional): String de conex√£o do MongoDB
            database_name (str, optional): Nome do banco de dados
            environment (str, optional): Ambiente espec√≠fico ('local', 'docker_host', 'docker_container', 'atlas')
        """
        # Auto-detectar configura√ß√£o se n√£o fornecida
        if connection_string is None or database_name is None:
            if environment:
                config = MongoConfig.get_config(environment)
            else:
                config = MongoConfig.auto_detect_environment()
            
            self.connection_string = connection_string or config['connection_string']
            self.database_name = database_name or config['database_name']
            self.environment_description = config['description']
        else:
            self.connection_string = connection_string
            self.database_name = database_name
            self.environment_description = "Configura√ß√£o personalizada"
        self.client = None
        self.db = None
        self.collection = None
        
    def connect(self):
        """Estabelece conex√£o com o MongoDB"""
        try:
            print(f"üîå Conectando ao MongoDB...")
            print(f"   Ambiente: {self.environment_description}")
            print(f"   Database: {self.database_name}")
            
            self.client = MongoClient(self.connection_string, serverSelectionTimeoutMS=5000)
            # Testa a conex√£o
            self.client.admin.command('ping')
            self.db = self.client[self.database_name]
            self.collection = self.db['usuarios']
            print("‚úÖ Conex√£o com MongoDB estabelecida com sucesso!")
            return True
        except ConnectionFailure as e:
            print(f"‚ùå Erro ao conectar com MongoDB: {e}")
            print(f"   Verifique se o MongoDB est√° rodando e acess√≠vel")
            return False
        except Exception as e:
            print(f"‚ùå Erro inesperado: {e}")
            return False
    
    def disconnect(self):
        """Fecha a conex√£o com o MongoDB"""
        if self.client:
            self.client.close()
            print("üîå Conex√£o com MongoDB fechada.")
    
    # CREATE - Inserir documentos
    def create_user(self, nome, email, idade, cidade=None):
        """
        Cria um novo usu√°rio no banco de dados
        
        Args:
            nome (str): Nome do usu√°rio
            email (str): Email do usu√°rio
            idade (int): Idade do usu√°rio
            cidade (str, optional): Cidade do usu√°rio
            
        Returns:
            str: ID do documento inserido ou None se houver erro
        """
        try:
            documento = {
                "nome": nome,
                "email": email,
                "idade": idade,
                "cidade": cidade,
                "data_criacao": datetime.now(),
                "ativo": True
            }
            
            resultado = self.collection.insert_one(documento)
            print(f"‚úÖ Usu√°rio criado com sucesso! ID: {resultado.inserted_id}")
            return str(resultado.inserted_id)
            
        except DuplicateKeyError:
            print("‚ùå Erro: Email j√° existe no banco de dados")
            return None
        except Exception as e:
            print(f"‚ùå Erro ao criar usu√°rio: {e}")
            return None
    
    def create_multiple_users(self, usuarios):
        """
        Cria m√∫ltiplos usu√°rios de uma vez
        
        Args:
            usuarios (list): Lista de dicion√°rios com dados dos usu√°rios
            
        Returns:
            list: Lista de IDs dos documentos inseridos
        """
        try:
            for usuario in usuarios:
                usuario['data_criacao'] = datetime.now()
                usuario['ativo'] = True
                
            resultado = self.collection.insert_many(usuarios)
            print(f"‚úÖ {len(resultado.inserted_ids)} usu√°rios criados com sucesso!")
            return [str(id) for id in resultado.inserted_ids]
            
        except Exception as e:
            print(f"‚ùå Erro ao criar usu√°rios: {e}")
            return []
    
    # READ - Ler documentos
    def read_all_users(self):
        """
        L√™ todos os usu√°rios do banco de dados
        
        Returns:
            list: Lista de todos os usu√°rios
        """
        try:
            usuarios = list(self.collection.find())
            print(f"üìñ Encontrados {len(usuarios)} usu√°rios")
            return usuarios
        except Exception as e:
            print(f"‚ùå Erro ao ler usu√°rios: {e}")
            return []
    
    def read_user_by_id(self, user_id):
        """
        L√™ um usu√°rio espec√≠fico pelo ID
        
        Args:
            user_id (str): ID do usu√°rio
            
        Returns:
            dict: Dados do usu√°rio ou None se n√£o encontrado
        """
        try:
            from bson import ObjectId
            usuario = self.collection.find_one({"_id": ObjectId(user_id)})
            if usuario:
                print(f"üìñ Usu√°rio encontrado: {usuario['nome']}")
            else:
                print("‚ùå Usu√°rio n√£o encontrado")
            return usuario
        except Exception as e:
            print(f"‚ùå Erro ao buscar usu√°rio: {e}")
            return None
    
    def read_users_by_filter(self, filtro):
        """
        L√™ usu√°rios com base em um filtro
        
        Args:
            filtro (dict): Filtro para busca
            
        Returns:
            list: Lista de usu√°rios que atendem ao filtro
        """
        try:
            usuarios = list(self.collection.find(filtro))
            print(f"üìñ Encontrados {len(usuarios)} usu√°rios com o filtro aplicado")
            return usuarios
        except Exception as e:
            print(f"‚ùå Erro ao buscar usu√°rios: {e}")
            return []
    
    # UPDATE - Atualizar documentos
    def update_user(self, user_id, novos_dados):
        """
        Atualiza um usu√°rio espec√≠fico
        
        Args:
            user_id (str): ID do usu√°rio
            novos_dados (dict): Novos dados para atualiza√ß√£o
            
        Returns:
            bool: True se atualizado com sucesso, False caso contr√°rio
        """
        try:
            from bson import ObjectId
            novos_dados['data_atualizacao'] = datetime.now()
            
            resultado = self.collection.update_one(
                {"_id": ObjectId(user_id)},
                {"$set": novos_dados}
            )
            
            if resultado.modified_count > 0:
                print(f"‚úÖ Usu√°rio atualizado com sucesso!")
                return True
            else:
                print("‚ùå Nenhum usu√°rio foi atualizado")
                return False
                
        except Exception as e:
            print(f"‚ùå Erro ao atualizar usu√°rio: {e}")
            return False
    
    def update_multiple_users(self, filtro, novos_dados):
        """
        Atualiza m√∫ltiplos usu√°rios com base em um filtro
        
        Args:
            filtro (dict): Filtro para sele√ß√£o dos usu√°rios
            novos_dados (dict): Novos dados para atualiza√ß√£o
            
        Returns:
            int: N√∫mero de documentos atualizados
        """
        try:
            novos_dados['data_atualizacao'] = datetime.now()
            
            resultado = self.collection.update_many(
                filtro,
                {"$set": novos_dados}
            )
            
            print(f"‚úÖ {resultado.modified_count} usu√°rios atualizados")
            return resultado.modified_count
            
        except Exception as e:
            print(f"‚ùå Erro ao atualizar usu√°rios: {e}")
            return 0
    
    # DELETE - Deletar documentos
    def delete_user(self, user_id):
        """
        Deleta um usu√°rio espec√≠fico
        
        Args:
            user_id (str): ID do usu√°rio
            
        Returns:
            bool: True se deletado com sucesso, False caso contr√°rio
        """
        try:
            from bson import ObjectId
            resultado = self.collection.delete_one({"_id": ObjectId(user_id)})
            
            if resultado.deleted_count > 0:
                print(f"‚úÖ Usu√°rio deletado com sucesso!")
                return True
            else:
                print("‚ùå Nenhum usu√°rio foi deletado")
                return False
                
        except Exception as e:
            print(f"‚ùå Erro ao deletar usu√°rio: {e}")
            return False
    
    def delete_users_by_filter(self, filtro):
        """
        Deleta m√∫ltiplos usu√°rios com base em um filtro
        
        Args:
            filtro (dict): Filtro para sele√ß√£o dos usu√°rios
            
        Returns:
            int: N√∫mero de documentos deletados
        """
        try:
            resultado = self.collection.delete_many(filtro)
            print(f"‚úÖ {resultado.deleted_count} usu√°rios deletados")
            return resultado.deleted_count
            
        except Exception as e:
            print(f"‚ùå Erro ao deletar usu√°rios: {e}")
            return 0
    
    def delete_all_users(self):
        """
        Deleta todos os usu√°rios (usar com cuidado!)
        
        Returns:
            int: N√∫mero de documentos deletados
        """
        try:
            resultado = self.collection.delete_many({})
            print(f"‚úÖ Todos os {resultado.deleted_count} usu√°rios foram deletados")
            return resultado.deleted_count
            
        except Exception as e:
            print(f"‚ùå Erro ao deletar todos os usu√°rios: {e}")
            return 0
    
    # M√©todos auxiliares
    def count_users(self):
        """
        Conta o n√∫mero total de usu√°rios
        
        Returns:
            int: N√∫mero de usu√°rios
        """
        try:
            count = self.collection.count_documents({})
            print(f"üìä Total de usu√°rios: {count}")
            return count
        except Exception as e:
            print(f"‚ùå Erro ao contar usu√°rios: {e}")
            return 0
    
    def print_users(self, usuarios):
        """
        Imprime usu√°rios de forma formatada
        
        Args:
            usuarios (list): Lista de usu√°rios para imprimir
        """
        if not usuarios:
            print("üìù Nenhum usu√°rio para exibir")
            return
            
        print("\n" + "="*80)
        print("üìã LISTA DE USU√ÅRIOS")
        print("="*80)
        
        for i, usuario in enumerate(usuarios, 1):
            print(f"\n{i}. ID: {usuario.get('_id')}")
            print(f"   Nome: {usuario.get('nome')}")
            print(f"   Email: {usuario.get('email')}")
            print(f"   Idade: {usuario.get('idade')}")
            print(f"   Cidade: {usuario.get('cidade', 'N√£o informado')}")
            print(f"   Ativo: {usuario.get('ativo', True)}")
            print(f"   Criado em: {usuario.get('data_criacao', 'N/A')}")
            if 'data_atualizacao' in usuario:
                print(f"   Atualizado em: {usuario.get('data_atualizacao')}")
        
        print("\n" + "="*80)


